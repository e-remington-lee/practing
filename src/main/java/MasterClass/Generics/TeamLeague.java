package main.java.MasterClass.Generics;


public class TeamLeague {
    private String name;
    private int rank;

    public TeamLeague(String name, int rank) {
        this.name = name;
        this.rank = rank;
    }

    public String getName() {
        return name;
    }

    public int getRank() {
        return rank;
    }
}
